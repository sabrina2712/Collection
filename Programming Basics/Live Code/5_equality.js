// Strictly Equal vs Loosely equal
// Not strict tries to convert type so beware!
// Go with strict equal and not strict equal (=== && !==)
console.log("Not strict: ", 20 == "20");
console.log("Strict: ", 20 === "20");
console.log("Not strict not equal: ", 20 != "20");
console.log("Strictly not equal: ", 20 !== "20");
